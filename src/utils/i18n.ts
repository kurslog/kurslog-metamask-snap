export function t(key: string, locale = 'uk'): string {
  return translations[locale]?.[key] || translations.uk[key] || key;
}

export function getLocaleName(
  item: { name_uk: string; name_ru: string; name_en: string },
  locale: string,
): string {
  const nameKey = `name_${locale}` as keyof typeof item;
  return (item[nameKey] as string) || item.name_en;
}
